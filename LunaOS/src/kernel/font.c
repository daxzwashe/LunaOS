// Функция теперь честно находит нужную букву по коду
const uint8_t* GetCharBitmap(char c) {
    // В таблице ASCII пробел имеет номер 32.
    // Наш массив начинается с пробела (индекс 0).
    // Значит формула: индекс = код_буквы - 32.
    
    if (c < 32 || c > 126) {
        return &font8x16[0]; // Если символ неизвестный, возвращаем пробел (или можно квадратик)
    }

    // 16 байт на один символ
    return &font8x16[(c - 32) * 16];
}